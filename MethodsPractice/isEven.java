package MethodsPractice;

public class isEven {

	    public static boolean IsEven(int num) {
	        return num % 2 == 0;
	    }

	    public static void main(String[] args) {
	        System.out.println("Is 4 even? " + IsEven (4));
	        System.out.println("Is 7 even? " +IsEven(7));
	    }
	}
