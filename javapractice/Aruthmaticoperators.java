package javapractice;

public class Aruthmaticoperators {

	public static void main(String[] args) {

		int a = 10;
		int b = 20;
		System.out.println("a + b = " + (a + b) );
		System.out.println("a - b = " + (a - b) );
		System.out.println("a * b = " + (a * b) );
		System.out.println("a / b = " + (a / b) );
		System.out.println("a % b = " + (a % b) );
		System.out.println("a++ = " + (a++) );
		System.out.println("++b = " + (++b) );
		
		
		double x = 15.0;
		double y = 10.0;
		System.out.println("x / y = " + (x /y));
	}

}
